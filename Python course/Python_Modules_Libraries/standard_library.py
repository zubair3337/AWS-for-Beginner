# Python Standard Library Example
# The standard library is like built-in tools that come with Python.

import math  # like a built-in calculator tool

# Example: finding square root
# Imagine you want to know how many people can sit evenly in rows (square root)
number = 16
sqrt_value = math.sqrt(number)
print(f"The square root of {number} is {sqrt_value}")  # Output: 4.0
