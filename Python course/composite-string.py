# Primitive data types for the movie "Inception"

#Primitive Data Types: Best for representing individual, simple pieces of data.
#Composite Data Types: Useful for organizing and grouping complex, structured data (e.g., managing a system, collection, or entity relationships).

movie_name = "Inception"  # String
release_year = 2010       # Integer
have_seen = True          # Boolean

# Display the movie information
print(f"Movie: {movie_name}")
print(f"Year Released: {release_year}")
print(f"Have I seen it? {have_seen}")

print(f"Length of the movie name: {len(movie_name)}")

print(f"Uppercase: {movie_name.upper()}")

print(f"Does the name contain 'In'? {'In' in movie_name}")

